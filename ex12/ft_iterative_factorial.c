int	ft_iterative_factorial(int nb)
{
	int	r;

	r = 1;
	if (nb < 0)
		return (0);
	if (nb < 1)
		return (1);
	while (nb > 1)
	{
		if (r > (~(1 << 31) / nb))
			return (0);
		r *= nb;
		nb--;
	}
	return (r);
}
